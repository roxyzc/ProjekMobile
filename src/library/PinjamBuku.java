    private void btnPinjamActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPinjamActionPerformed
        // TODO add your handling code here:
        String no_buku = txtNoBuku.getText();
        if(!no_buku.isEmpty() && !no_buku.isBlank()){
            try {
                Connection conn = Koneksi.ConnectDB();
                String query = "SELECT * FROM tb_buku where no_bk='"+ no_buku +"'";
                Statement st = conn.createStatement();
                ResultSet rs = st.executeQuery(query);
                String Astok_buku = null;
                while(rs.next()){
                    Astok_buku = rs.getString("stok_bk");
                }
                if(Astok_buku == null){
                    JOptionPane.showMessageDialog(null, "Oopss...  Buku Tidak terdaftar!", "Gagal", JOptionPane.ERROR_MESSAGE);
                    System.out.println("Buku Gagal dipinjam...");
                }else if("0".equals(Astok_buku)){
                    JOptionPane.showMessageDialog(null, "Oopss...  Stok Buku sedang kosong!", "Gagal", JOptionPane.ERROR_MESSAGE);
                    System.out.println("Buku Gagal dipinjam...");
                }else{
                    String sql = "UPDATE `tb_buku` SET `stok_bk`='"+ (Integer.parseInt(Astok_buku) - 1) +"' WHERE `no_bk`='"+ no_buku +"'";
                    PreparedStatement preparedStmt = conn.prepareStatement(sql);
                    preparedStmt.execute();
                    
                    String queryy = "SELECT * FROM tb_peminjam where no_bk='"+ no_buku +"' AND userID='"+Library.session.getSession()+"'";
                    Statement stt = conn.createStatement();
                    ResultSet rss = stt.executeQuery(queryy);
                    String Ajumlah = null;
                    while(rss.next()){
                        Ajumlah = rss.getString("jumlah_bk");
                    }
                    if(Ajumlah == null){
                        String insrt = "INSERT INTO `tb_peminjam`(`userID`, `no_bk`, `jumlah_bk`) VALUES ('"+Library.session.getSession()+"','"+no_buku+"', '1')";
                        PreparedStatement preStmt = conn.prepareStatement(insrt);
                        preStmt.execute();
                    }else{
                        String update = "UPDATE `tb_peminjam` SET `jumlah_bk`='"+ (Integer.parseInt(Ajumlah) + 1) +"' WHERE `no_bk`='"+ no_buku +"'";
                        PreparedStatement prs = conn.prepareStatement(update);
                        prs.execute();
                    }
                    conn.close();
                    System.out.println("Buku Berhasil dipinjam...");
                    JOptionPane.showMessageDialog(null, "Success...  Buku Berhasil dipinjam!", "Succses", JOptionPane.INFORMATION_MESSAGE);
                    txtNoBuku.setText("");
                }
            } catch (SQLException e) {
                System.out.println("Buku Gagal dipinjam...");
                System.out.println(e);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Oopss...  No.Buku harus diisi!", "Warning", JOptionPane.INFORMATION_MESSAGE);
            System.out.println("Buku Gagal dipinjam...");
        }
        showTableBuku();
    }//GEN-LAST:event_btnPinjamActionPerformed

